import https from 'https';

const SUPABASE_URL = process.env.NEXT_PUBLIC_SUPABASE_URL;
const SERVICE_KEY = process.env.SUPABASE_SERVICE_ROLE_KEY;

if (!SUPABASE_URL || !SERVICE_KEY) {
  console.error('Missing env vars. Run with: node --env-file=.env.local scripts/seed-hsk-expanded-words.mjs');
  process.exit(1);
}

const SUPABASE_HOST = new URL(SUPABASE_URL).hostname;

function req(method, path, body, prefer = 'return=representation') {
  return new Promise((resolve, reject) => {
    const data = body ? JSON.stringify(body) : null;
    const opts = {
      hostname: SUPABASE_HOST, port: 443,
      path: '/rest/v1' + path, method,
      headers: {
        'Content-Type': 'application/json',
        Authorization: 'Bearer ' + SERVICE_KEY,
        apikey: SERVICE_KEY,
        Prefer: prefer,
        ...(data ? { 'Content-Length': Buffer.byteLength(data) } : {}),
      },
    };
    const r = https.request(opts, (res) => {
      let buf = '';
      res.on('data', (c) => { buf += c; });
      res.on('end', () => resolve({ status: res.statusCode, body: buf }));
    });
    r.on('error', reject);
    if (data) r.write(data);
    r.end();
  });
}

