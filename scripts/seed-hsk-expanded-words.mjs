const EXPANSIONS = [
  { courseName: 'HSK 1 — Absolute Beginner', words: HSK1_WORDS, hskLevel: 1 },
  { courseName: 'HSK 2 — Elementary', words: HSK2_WORDS, hskLevel: 2 },
  { courseName: 'HSK 3 — Intermediate', words: HSK3_WORDS, hskLevel: 3 },
  { courseName: 'HSK 4 — Upper Intermediate', words: HSK4_WORDS, hskLevel: 4 },
  { courseName: 'HSK 5 — Advanced', words: HSK5_WORDS, hskLevel: 5 },
  { courseName: 'HSK 6 — Mastery', words: HSK6_WORDS, hskLevel: 6 },
];

async function main() {
  console.log('📚 Expanding HSK courses with comprehensive vocabulary…\n');

  for (const exp of EXPANSIONS) {
    // Find course ID
    const findRes = await req('GET', `/vocabulary_courses?select=id&creator_id=is.null&name=eq.${encodeURIComponent(exp.courseName)}`);
    let courseId;
    try {
      const rows = JSON.parse(findRes.body);
      courseId = rows?.[0]?.id;
    } catch { /* ignore */ }

    if (!courseId) {
      console.error(`  ❌ Course not found: "${exp.courseName}" — run seed-public-courses.mjs first`);
      continue;
    }

    // Get existing word_zh values to skip duplicates
    const existingRes = await req('GET', `/course_vocabulary_items?select=word_zh&course_id=eq.${courseId}`);
    let existingZh = new Set();
    try {
      const rows = JSON.parse(existingRes.body);
      existingZh = new Set(rows.map((r) => r.word_zh));
    } catch { /* ignore */ }

    // Get current max sort_order
    const maxSortRes = await req('GET', `/course_vocabulary_items?select=sort_order&course_id=eq.${courseId}&order=sort_order.desc&limit=1`);
    let startOrder = 100;
    try {
      const rows = JSON.parse(maxSortRes.body);
      startOrder = (rows?.[0]?.sort_order ?? 99) + 1;
    } catch { /* ignore */ }

    const newWords = exp.words.filter((w) => !existingZh.has(w.word_zh));

    if (newWords.length === 0) {
      console.log(`  ⏭️  "${exp.courseName}" — all words already present`);
      continue;
    }

    const items = newWords.map((w, i) => ({
      course_id: courseId,
      word_zh: w.word_zh,
      word_pinyin: w.word_pinyin,
      word_en: w.word_en,
      example_sentence: w.example_sentence,
      hsk_level: exp.hskLevel,
      sort_order: startOrder + i,
    }));

    // Insert in batches of 50
    let inserted = 0;
    for (let i = 0; i < items.length; i += 50) {
      const batch = items.slice(i, i + 50);
      const ir = await req('POST', '/course_vocabulary_items', batch, 'resolution=ignore-duplicates,return=minimal');
      if (ir.status >= 300) {
        console.error(`   ❌ Batch failed: ${ir.body.slice(0, 120)}`);
      } else {
        inserted += batch.length;
      }
    }

    console.log(`  ✅ "${exp.courseName}" → +${inserted} words (total ~${existingZh.size + inserted})`);
  }

  // Update course descriptions to note "most important words" coverage
  console.log('\n📝 Updating course descriptions…');
  for (const upd of DESCRIPTION_UPDATES) {
    const findRes = await req('GET', `/vocabulary_courses?select=id&creator_id=is.null&name=eq.${encodeURIComponent(upd.courseName)}`);
    let courseId;
    try {
      const rows = JSON.parse(findRes.body);
      courseId = rows?.[0]?.id;
    } catch { /* ignore */ }

    if (!courseId) {
      console.error(`  ❌ Course not found for description update: "${upd.courseName}"`);
      continue;
    }

    const dr = await req('PATCH', `/vocabulary_courses?id=eq.${courseId}`, { description: upd.description }, 'return=minimal');
    if (dr.status >= 300) {
      console.error(`  ❌ Description update failed for "${upd.courseName}": ${dr.body.slice(0, 120)}`);
    } else {
      console.log(`  ✅ "${upd.courseName}" description updated`);
    }
  }

  console.log('\n🎉 Done!');
}

main().catch((err) => { console.error('❌', err.message); process.exit(1); });
